# Basics of calc

x = input("Enter the first number : ")
y = input("Enter the second number : ")

# Type casting

add = int(x) + int(y)
sub = int(x) - int(y)
mul = int(x) * int(y)
div = int(x) / int(y)

print("addition is: ", add)
print("subtraction is: ", sub)
print("multiplication is: ", mul)
print("division is: ", div)
